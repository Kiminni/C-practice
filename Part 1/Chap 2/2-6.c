#include <stdio.h>
int main(void)
{
	printf("10진수 정수: %d  \n",0.5); //.5는 반올림이라 버림
	printf("10진수 실수: %f  \n",0.5); //소수점 6자리까지 나타냄
	printf("10진수 실수: %lf \n",0.5); //소수점 6자리까지 나타냄

   	printf("소수점 이하 6자리 이상: %f  \n",0.5655678); //소수점 6자리 이상 반올림
	printf("소수점 이하 6자리 이상: %lf \n",0.5667784); //소수점 6자리 이상 반올림x 

	return 0;
}
 